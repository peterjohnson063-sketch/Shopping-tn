-- 6) KPI view for logistics analytics
drop view if exists public.logistics_kpis_v1;
create view public.logistics_kpis_v1 as
select
  30::int as days,
  count(*)::int as total_orders,
  count(*) filter (where lower(coalesce(status,'')) = 'delivered')::int as delivered_orders,
  count(*) filter (
    where lower(coalesce(status,'')) in ('confirmed','processing','ready','out_for_delivery','out-for-delivery','shipped','transit')
  )::int as active_orders,
  count(*) filter (where driver_id is not null)::int as assigned_orders,
  round(avg(extract(epoch from (driver_accepted_at - created_at))/60.0) filter (
    where driver_accepted_at is not null and created_at is not null and driver_accepted_at >= created_at
  )::numeric, 2) as avg_pickup_minutes
from public.orders;

grant select on public.logistics_kpis_v1 to anon, authenticated;

